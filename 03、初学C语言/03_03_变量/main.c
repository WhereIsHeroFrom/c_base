#include<stdio.h>

int main() {
    // 1、变量的定义
    // 数据类型 变量;
    int a;
    // 整型

    // 2、变量的赋值
    // 变量名 = 值;
    a = 520;
    printf("%d", a);

    // 3、变量初始化
    // 数据类型 变量名 = 值;
    int b = 1314;
    printf("%d", b);

    // 4、多变量定义
    // 数据类型 变量名1 = 值1, 变量名2 = 值2, 变量名3 = 值3;
    int c = 1, d = 2, e = 3;
    int f, g = 4, h;
    // printf("%d", f);
    return 0;
}